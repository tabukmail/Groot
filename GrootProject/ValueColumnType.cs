namespace Treeton
{
    /// <summary>
    /// Specifies the type of value that a column can hold.
    /// </summary>
    public enum ValueColumnType
    {
        /// <summary>
        /// A string value. --- :))
        /// </summary>
        String,

        /// <summary>
        /// An integer value.
        /// </summary>
        Integer,

        /// <summary>
        /// A floating-point value.
        /// </summary>
        Float,

        /// <summary>
        /// A double-precision floating-point value.
        /// </summary>
        Double,

        /// <summary>
        /// A boolean value.
        /// </summary>
        Boolean,

        /// <summary>
        /// A custom value type.
        /// </summary>
        Custom
    }
}
